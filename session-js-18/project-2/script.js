//iterative statements

//for statement
for (let start = 1; start > 2; start++) {
  console.log("enter name");
  console.log("age");
  console.log("enter email");
  console.log("save");
}

//white statement
let condition = 1;
while (condition > 2) {
  console.log("enter name");
  console.log("age");
  console.log("enter email");
  console.log("save");

  condition++;
}

//do-while loop
let m = 1;
do {
  console.log("loop executed");
  m++;
} while (m < 1);



let quote='welcome to javascript'

//length of string
console.log(quote.length)

//get substring
console.log(quote.substring(2,10))

//find char at specific index
console.log(quote.charAt(11))
console.log(quote[11])

//find index of a char
console.log(quote.indexOf('java'))

//case conversion
console.log(quote.toUpperCase())
console.log(quote.toLowerCase())

//join strings
let str1='hello'
let str2='man'
console.log(str1+str2)
console.log(str1.concat(str2))

//split string into array
console.log(quote.split(''))
